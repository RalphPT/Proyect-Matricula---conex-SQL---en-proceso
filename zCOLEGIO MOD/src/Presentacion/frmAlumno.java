    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if ("".equals(txtDni.getText())) {
            JOptionPane.showMessageDialog(null, "Es necesario llenar el campo DNI");
            txtDni.requestFocus();
        } else {
            objAlumno = alumnol.busca_Alumno(txtDni.getText());

            txtDni.setText(objAlumno.getDni());
            txtNombres.setText(objAlumno.getNomb_alum());
            txtApe_Paterno.setText(objAlumno.getApell_pat());
            txtApe_Materno.setText(objAlumno.getApell_mat());
            txtDomic.setText(objAlumno.getDireccion());
            ftxtFecha.setText(objAlumno.getFecha_nac());
            cboReligion.setSelectedIndex(objAlumno.getReligion());

            objApoderado = apoderadol.busca_Apoderado(txtDni.getText());

            txtA_Dni.setText(objApoderado.getA_Dni());
            txtNombres_Apellidos.setText(objApoderado.getA_Nombres_Apellidos());
            cboParentesco.setSelectedIndex(objApoderado.getTipo_Parentesco());
            txtDomicilio.setText(objApoderado.getA_Domicilio());
            txtCelular.setText(objApoderado.getA_Celular());
            txtTelfTrab.setText(objApoderado.getA_Telf_Trabajo());
            cboEstado_Civil.setSelectedIndex(objApoderado.getA_Estado_Civil());
            txtOcupacion.setText(objApoderado.getA_Ocupacion());
            txtTrabajo.setText(objApoderado.getA_Centro_Trabajo());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

