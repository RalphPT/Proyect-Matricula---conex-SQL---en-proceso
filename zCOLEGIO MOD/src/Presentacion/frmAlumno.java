    public void nuevo() {
        txtDni.setText("");
        txtNombres.setText("");
        txtApe_Paterno.setText("");
        txtApe_Materno.setText("");
        txtDomic.setText("");
        ftxtFecha.setText("AA/MM/DD");
        cboEstado_Civil.setSelectedIndex(0);
        cboParentesco.setSelectedIndex(0);
        cboReligion.setSelectedIndex(0);
        txtA_Dni.setText("");
        txtNombres_Apellidos.setText("");
        txtDomicilio.setText("");
        txtCelular.setText("");
        txtTelfTrab.setText("");
        txtOcupacion.setText("");
        txtTrabajo.setText("");
    }

    public void set_Alumno() {
        objAlumno.setDni(txtDni.getText());
        objAlumno.setNomb_alum(txtNombres.getText());
        objAlumno.setApell_pat(txtApe_Paterno.getText());
        objAlumno.setApell_mat(txtApe_Materno.getText());
        objAlumno.setFecha_nac(ftxtFecha.getText());
        objAlumno.setDireccion(txtDomic.getText());
        if (chkM.isSelected()) {
            objAlumno.setSexo("M");
        } else if (chkF.isSelected()) {
            objAlumno.setSexo("F");
        }
        objAlumno.setReligion(cboReligion.getSelectedIndex());
    }

    public void set_Apoderado() {

        objApoderado.setDni(txtDni.getText());
        objApoderado.setA_Nombres_Apellidos(txtNombres_Apellidos.getText());
        objApoderado.setA_Dni(txtA_Dni.getText());
        objApoderado.setA_Estado_Civil(cboEstado_Civil.getSelectedIndex());
        objApoderado.setA_Celular(txtCelular.getText());
        objApoderado.setA_Domicilio(txtDomicilio.getText());
        objApoderado.setA_Ocupacion(txtOcupacion.getText());
        objApoderado.setA_Centro_Trabajo(txtTrabajo.getText());
        objApoderado.setA_Telf_Trabajo(txtTelfTrab.getText());

        objApoderado.setTipo_Parentesco(cboParentesco.getSelectedIndex());
    }

